package section4;

public class Expressions {

  public static void main(String[] args) {
    //  Expressions:
    //    > in variables declaration, the content between the type and the semicolon, ex:
    //        String
    //          name = "David" // expression
    //        ;
    //    > string literals between double quotes, including them, ex:
    //        System.out.println(
    //          "David" // expression
    //        );
    //    > content between parenthesis, ex:
    //        if (
    //          1 == 1 // expression
    //        ){ }
    //
    //  Statements:
    //    > the complete line, til the semicolon, ex:
    //        String name = "David"; // statement
    //        System.out.println("David"); // statement

  }

}
